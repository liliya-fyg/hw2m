while True:
    color = input("Введите цвет светофора (зеленый, желтый, красный) или 'выход' для завершения: ").strip().lower()

    if color == "выход":
        print("Программа завершена.")
        break
    elif color == "зеленый":
        print("Можно идти!")
    elif color == "желтый":
        print("Подождите!")
    elif color == "красный":
        print("Стой!")
    else:
        print("Неверный ввод. Пожалуйста, введите: зеленый, желтый, красный или 'выход'. ")
